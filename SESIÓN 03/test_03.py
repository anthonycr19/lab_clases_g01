"""Listas en Python"""

"""Listas -> remove(): Elimina un elemento de la lista indicando el valor"""

var_1 = ["Python", "JAVA", "php", "Javascript", "Typescript"]

print(var_1)

var_1.remove("php")

print("Mi lista actualizada es: {}".format(var_1))

var_1.remove("JAVA")
print("Mi lista actualizada es: {}".format(var_1))